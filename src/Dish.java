
public class Dish {
    private String dname;
    private int surpuls;
    private int price;
	
    /**
     * get dish name
     * @return
     */ 
    public String getDname() {
		return dname;
	}
    /**
     * set dish name
     * @param dname
     */
	public void setDname(String dname) {
		this.dname = dname;
	}
	/**
	 * get surpuls
	 * @return
	 */
	public int getSurpuls() {
		return surpuls;
	}
	/**
	 * set surpuls
	 * @param surpuls
	 */
	public void setSurpuls(int surpuls) {
		this.surpuls = surpuls;
	}
	/**
	 * get price
	 * @return
	 */
	public int getPrice() {
		return price;
	}
	/**
	 * set price
	 * @param price
	 */
	public void setPrice(int price) {
		this.price = price;
	}
    
	
}
